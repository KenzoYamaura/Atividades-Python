'''
Roberto é um funcionário da imobiliária TrianMóbile e lida especificamente 
com a venda de terrenos retangulares. Ele precisa de um algoritmo para 
calcular a área desses terrenos com base em suas dimensões. Faça um 
algoritmo para ler as dimensões de um terreno e depois exibir a área do 
terreno.
'''

base = float(input("Base do Terreno: "))
altura = float(input("Altura do terreno: "))

area = base * altura

print(f"A área do terreno é: {area}")


